const TranslationContext = createContext();

export const useTranslation = () => {
    const context = useContext(TranslationContext);
    if (!context) {
        throw new Error('useTranslation must be used within a TranslationProvider');
    }
    return context;
};

export const TranslationProvider = ({ children }) => {
    const [currentLanguage, setCurrentLanguage] = useState('en');
    
    useEffect(() => {
        const savedLanguage = localStorage.getItem('selectedLanguage');
        if (savedLanguage && translations[savedLanguage]) {
            setCurrentLanguage(savedLanguage);
        }
    }, []);

    const changeLanguage = (languageCode) => {
        if (translations[languageCode]) {
            setCurrentLanguage(languageCode);
            localStorage.setItem('selectedLanguage', languageCode);
            // Reloading the page ensures all components, even those not actively re-rendering,
            // will get the new language strings. For a smoother UX, you could manage
            // this with state updates that trigger re-renders across the app.
            window.location.reload();
        }
    };

    const t = (key) => {
        const keys = key.split('.');
        let value = translations[currentLanguage];
        
        // Traverse the nested object to find the translation string
        for (const k of keys) {
            if (value && typeof value === 'object' && k in value) {
                value = value[k];
            } else {
                value = undefined; // Key path not found
                break;
            }
        }
        
        // Fallback to English if the translation is not found in the current language
        if (value === undefined) {
            let fallbackValue = translations.en;
            for (const k of keys) {
                if (fallbackValue && typeof fallbackValue === 'object' && k in fallbackValue) {
                    fallbackValue = fallbackValue[k];
                } else {
                    return key; // Return the key itself if not found in English either
                }
            }
            return fallbackValue;
        }
        
        return value;
    };

    const getRandomWelcomeMessage = () => {
        const messages = t('welcomeMessages');
        // Ensure messages is an array before trying to access an index
        if (Array.isArray(messages)) {
            return messages[Math.floor(Math.random() * messages.length)];
        }
        // Fallback if the welcomeMessages key is somehow not an array
        return t('welcomeMessages.0') || 'Welcome!';
    };

    return (
        <TranslationContext.Provider value={{
            currentLanguage,
            changeLanguage,
            t,
            getRandomWelcomeMessage
        }}>
            {children}
        </TranslationContext.Provider>
    );
};

export default TranslationProvider;
